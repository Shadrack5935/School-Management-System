    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int checkUserExist = 0;
        String Username = txtStudent.getText();
        if (Username.equals("")) {
            JOptionPane.showMessageDialog(null, "Student name field is required");
        } else {
            SimpleDateFormat dFormat = new SimpleDateFormat("dd-mm-yy");
            try {
                Connection Con = ConnectionProvider.getCon();
                Statement st = Con.createStatement();
                ResultSet rs = st.executeQuery("select *from students where studentname='" + Username + "'");
                while (rs.next()) {
                    txtStudent.setEditable(true);
                    checkUserExist = 1;
                    txtlevel.setText(rs.getString("class"));
                    txtFname.setText(rs.getString("fatherName"));
                    txtMname.setText(rs.getString("motherName"));
                    txtMobile.setText(rs.getString("mobileNumber"));
                    String dob = rs.getString("DOB");
                    if (dob != "") {
                        try {
                            dateDOB.setDate(dFormat.parse(dob));
                        } catch (Exception e) {
                        }
                    }

                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            if (checkUserExist == 0) {
                JOptionPane.showMessageDialog(null, "Student name Does Not Exist");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

