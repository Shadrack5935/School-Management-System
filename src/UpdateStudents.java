    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
       String StudentName = txtStudent.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-mm-yyy");
        Date date = dateDOB.getDate();
        String dob = "";
        if (date != null) {
            dob = dFormat.format(dateDOB.getDate());
        }
        String Mobilenumber = txtMobile.getText();
        String level = txtlevel.getText();
        String father = txtFname.getText();
        String mother = txtMname.getText();
        if (StudentName.equals("")) {
            JOptionPane.showMessageDialog(null, " Student Name filled is required");
        } else if (dob.equals("")) {
            JOptionPane.showMessageDialog(null, "DOB filled is required");
        } else if (Mobilenumber.equals("")) {
            JOptionPane.showMessageDialog(null, "Mobile number is  filled is required");
        } else if (!Mobilenumber.matches( Mobilenumberpattern) || Mobilenumber.length() != 10) {
            JOptionPane.showMessageDialog(null, "Mobile Number filled is invalid");
        } else if (father.equals("")) {
            JOptionPane.showMessageDialog(null, "Father Name  filled is required");
        } else if (mother.equals("")) {
            JOptionPane.showMessageDialog(null, "Mother Name filled is required ");
        } 
         else if (level.equals("")) {
            JOptionPane.showMessageDialog(null, "class filled is required");
        } else {
            try {
                Connection Con = ConnectionProvider.getCon();
                PreparedStatement ps = Con.prepareStatement("update students set fatherName=?,DOB=?,mobileNumber=?,motherName=?,class=? where studentName=?");
                ps.setString(1,father);
                ps.setString(2,dob);
                ps.setString(3,Mobilenumber);
                ps.setString(4,mother);
                ps.setString(5,level);
                ps.setString(6,StudentName);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Student updated successfuly");
                setVisible(false);
                new UpdateStudents().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }




    }//GEN-LAST:event_jButton3ActionPerformed

